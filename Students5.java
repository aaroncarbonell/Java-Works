
public class Students5 {
	private String name;
	private int id;
	private String major;
	private double gpa;
	private String town;
	public Students5(String name, int id, String major, double gpa, String town) {
		super();
		this.name = name;
		this.id = id;
		this.major = major;
		this.gpa = gpa;
		this.town = town;
	}
	public String getName() {
		return name;
	}
	public int getId() {
		return id;
	}
	public String getMajor() {
		return major;
	}
	public double getGpa() {
		return gpa;
	}
	public String getTown() {
		return town;
	}
	
}